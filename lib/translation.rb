  def text(*keys)
    disp_lang = respond_to?('session') ? session[:display_language] : nil
    dict = if disp_lang == "Swedish"
             SwedishDictionary
           else
             EnglishDictionary
           end
    keys = [keys] if keys.class != Array
    keys.map { |key| dict[key] || key.to_s }.join(' ')
  end
